/**
 * 나라 이름을 받아서 해당하는 국기 이모지를 반환합니다.
 * @param countryName 나라 이름 (한국어)
 * @returns 국기 이모지 문자열, 매핑되지 않은 경우 기본 이모지 반환
 */
export const getCountryFlag = (countryName: string): string => {
  return COUNTRY_NAME_TO_FLAG[countryName] || "🏳️";
};

/**
 * 백엔드 API에서 받은 도시 데이터를 프론트엔드에서 사용하는 City 타입으로 변환합니다.
 * @param apiData 백엔드 API에서 받은 도시 데이터
 * @returns 프론트엔드에서 사용하는 City 객체
 */
export const transformApiDataToCity = (apiData: {
  cityId: number;
  cityName: string;
  countryName: string;
  lat: number;
  lng: number;
  countryCode: string;
}) => {
  return {
    id: apiData.cityId.toString(),
    name: apiData.cityName,
    country: apiData.countryName,
    flag: getCountryFlag(apiData.countryName),
    lat: apiData.lat,
    lng: apiData.lng,
    countryCode: apiData.countryCode,
  };
};
